「かっこいい大人の男」ってなに？となったときに、
ありきたりな答えになってしまいますが、

やはり
好きなことに取り組み、
そして挑戦している姿は
「輝いているし、かっこいい」

という答えにたどり着きました。

ただ、
輝けるくらいことって、何だろう？
なんでもかんでも挑戦することがかっこいいことなのか？

と思ったときに、
「それは違うな...」となりました。

輝けるくらいことってやっぱり、

そのことが好きで好きでたまらなくて、
勝手に体が動いてしまう、
勝手に手が動いて調べてしまう、

「1」しか聞かれてないのに、
「10」も「20」も答えてしまって
聞かれた人に引かれてしまうような...

所謂、「偏愛」。

その「偏愛」を大切にし、
突き詰めていくことこそが、
現時点での〇人の男たちの
「かっこいい大人の男」の答えです。

ただ、なぜblogという形で世に出すかというと、

